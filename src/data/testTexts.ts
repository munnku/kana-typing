/**
 * Build a long shuffled text from sentences, looped to fill `targetChars` kana chars.
 */
export function buildTestText(targetChars: number): string {
  const shuffled = [...TEST_SENTENCES].sort(() => Math.random() - 0.5);
  let result = '';
  let i = 0;
  while (result.replace(/[ 、。]/g, '').length < targetChars) {
    result += (result ? ' ' : '') + shuffled[i % shuffled.length];
    i++;
  }
  return result;
}
