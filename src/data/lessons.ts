export const LESSONS_BY_ID: Record<string, Lesson> = Object.fromEntries(
  LESSONS.map(l => [l.id, l])
);

export function getNextLesson(lessonId: string): Lesson | null {
  const idx = LESSONS.findIndex(l => l.id === lessonId);
  if (idx === -1 || idx >= LESSONS.length - 1) return null;
  return LESSONS[idx + 1];
}

export function getLessonsByUnit(unitId: string): Lesson[] {
  return LESSONS.filter(l => l.unitId === unitId);
}

export function getUnitIds(): string[] {
  return Array.from(new Set(LESSONS.map(l => l.unitId)));
}
